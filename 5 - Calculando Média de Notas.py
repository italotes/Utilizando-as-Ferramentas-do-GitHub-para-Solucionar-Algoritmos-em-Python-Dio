# Solicita ao usuário que digite a primeira nota e converte para float
nota1 = float(input("Digite a primeira nota: "))

# Solicita ao usuário que digite a segunda nota e converte para float
nota2 = float(input("Digite a segunda nota: "))

# Solicita ao usuário que digite a terceira nota e converte para float
nota3 = float(input("Digite a terceira nota: "))

# Calcula a média das três notas usando o operador aritmético de soma e divisão
media = (nota1 + nota2 + nota3) / 3

# Exibe o resultado da média
print("A média das notas é:", media)